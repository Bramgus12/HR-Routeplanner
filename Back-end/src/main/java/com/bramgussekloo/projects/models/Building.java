package com.bramgussekloo.projects.models;

import com.fasterxml.jackson.annotation.JsonBackReference;
import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonManagedReference;
import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;

import java.util.HashSet;
import java.util.Set;

import javax.persistence.*;

@ApiModel(description = "The model of the building")
@Entity
public class Building {

    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    @ApiModelProperty(notes = "Autogenerated id of the building")
    private Integer id;

    @ManyToOne(fetch = FetchType.EAGER)
    @JoinColumn(name = "address_id")
    @ApiModelProperty(notes = "Id of the address", required = true)
    private Address address;

    @ApiModelProperty(notes = "Name of the building", required = true)
    private String name;

    @ManyToMany(
            fetch = FetchType.LAZY,
            cascade = {
                    CascadeType.PERSIST,
                    CascadeType.MERGE
            }
    )
    @JoinTable(
            name = "building_institute",
            joinColumns = { @JoinColumn(name = "building_id") },
            inverseJoinColumns = { @JoinColumn(name = "institute_id") }
    )
    private Set<Institute> institutes = new HashSet<>();

    public Building(Integer id, Address address, String name) {
        this.address = address;
        this.id = id;
        this.name = name;
    }

    public Building() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Address getAddress() {
        return address;
    }

    public void setAddress(Address address) {
        this.address = address;
    }

    public Set<Institute> getInstitutes() {
        return institutes;
    }

    public void setInstitutes(Set<Institute> institutes) {
        this.institutes = institutes;
    }
}
